def print_cards(cards):
    message = ""
    for card in cards:
        message += f"  ___   "
    print(message)
    message = ""
    for card in cards:
        if len(card['card']) >= 2:
            message += f" |{card['card']} |  "
        else:
            message += f" |{card['card']}  |  "
    print(message)
    message = ""
    for card in cards:
        message += f" | {card['suit']} |  "
    print(message)
    message = ""
    for card in cards:
        if len(card['card']) >= 2:
            message += f" |_{card['card']}|  "
        else:
            message += f" |__{card['card']}|  "
    print(message + '\n')
    message = ""


